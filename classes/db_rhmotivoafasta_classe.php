  // funcao do sql 
  function sql_query($rh172_sequencial = null, $campos = "rhmotivoafasta.rh172_sequencial,*", $ordem = null, $dbwhere = "")
  {
    $sql = "select ";
    if ($campos != "*") {
      $campos_sql = explode("#", $campos);
      $virgula = "";
      for ($i = 0; $i < sizeof($campos_sql); $i++) {
        $sql .= $virgula . $campos_sql[$i];
        $virgula = ",";
      }
    } else {
      $sql .= $campos;
    }
    $sql .= " from rhmotivoafasta ";
    $sql2 = "";
    if ($dbwhere == "") {
      if ($rh172_sequencial != "" && $rh172_sequencial != null) {
        $sql2 = " where rhmotivoafasta.rh172_sequencial = '$rh172_sequencial'";
      }
    } else if ($dbwhere != "") {
      $sql2 = " where $dbwhere";
    }
    $sql .= $sql2;
    if ($ordem != null) {
      $sql .= " order by ";
      $campos_sql = explode("#", $ordem);
      $virgula = "";
      for ($i = 0; $i < sizeof($campos_sql); $i++) {
        $sql .= $virgula . $campos_sql[$i];
        $virgula = ",";
      }
    }
    return $sql;
  }

