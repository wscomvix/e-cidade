       }
     }else{
       $sql .= $campos;
     }
     $sql .= " from arrecad_itbi ";
     $sql .= "      inner join arrecad  on  arrecad.k00_numpre = arrecad_itbi.k00_numpre";
     $sql .= "      inner join itbi  on  itbi.it01_guia = arrecad_itbi.it01_guia";
