         $sql .= $virgula.$campos_sql[$i];
         $virgula = ",";
       }
     }else{
       $sql .= $campos;
     }
     $sql .= " from tipoquestaoaudit ";
     $sql2 = "";
     if($dbwhere==""){
       if( $ci01_codtipo != "" && $ci01_codtipo != null){
          $sql2 = " where tipoquestaoaudit.ci01_codtipo = $ci01_codtipo";
       }
     }else if($dbwhere != ""){
       $sql2 = " where $dbwhere";
     }
     $sql .= $sql2;
     if($ordem != null ){
       $sql .= " order by ";
       $campos_sql = split("#",$ordem);
       $virgula = "";
       for($i=0;$i<sizeof($campos_sql);$i++){
         $sql .= $virgula.$campos_sql[$i];
