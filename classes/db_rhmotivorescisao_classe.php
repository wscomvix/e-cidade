                $sql .= $virgula . $campos_sql[$i];
                $virgula = ",";
            }
        } else {
            $sql .= $campos;
        }
        $sql .= " from rhmotivorescisao ";
        $sql2 = "";
        if ($dbwhere == "") {
            if ($rh173_sequencial != "" && $rh173_sequencial != null) {
                $sql2 = " where rhmotivorescisao.rh173_sequencial = '$rh173_sequencial'";
            }
        } elseif ($dbwhere != "") {
            $sql2 = " where $dbwhere";
        }
        $sql .= $sql2;
        if ($ordem != null) {
            $sql .= " order by ";
            $campos_sql = explode("#", $ordem);
            $virgula = "";
            for ($i = 0; $i < sizeof($campos_sql); $i++) {
                $sql .= $virgula . $campos_sql[$i];
                $virgula = ",";
            }
        }
        return $sql;
    }

